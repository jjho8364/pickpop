package com.koiware.pickpop.domain;

public class SizeVO {
	int idsize;
	String namesize;
	int countsize;
	String detailsize;
	int idcategorysml;
	public int getIdsize() {
		return idsize;
	}
	public void setIdsize(int idsize) {
		this.idsize = idsize;
	}
	public String getNamesize() {
		return namesize;
	}
	public void setNamesize(String namesize) {
		this.namesize = namesize;
	}
	public int getCountsize() {
		return countsize;
	}
	public void setCountsize(int countsize) {
		this.countsize = countsize;
	}
	public String getDetailsize() {
		return detailsize;
	}
	public void setDetailsize(String detailsize) {
		this.detailsize = detailsize;
	}
	public int getIdcategorysml() {
		return idcategorysml;
	}
	public void setIdcategorysml(int idcategorysml) {
		this.idcategorysml = idcategorysml;
	}
	
	
}
